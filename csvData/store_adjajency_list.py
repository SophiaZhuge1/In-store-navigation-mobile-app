G = nx.Graph()

for i in range(len(adjList)):
    G.add_node(i+1)

for i in range(len(adjList)):
    for j in range(len(adjList[i])):
        G.add_edge(i+1, adjList[i][j])

adjMat = nx.to_numpy_matrix(G)
adjMatArr = numpy.asarray(adjMat)


def writeMatToCSV(mat):
    with open('csvData/adjMat.csv', 'w') as f:
        for i in range(len(mat)):
            for j in range(len(mat[i])):
                if mat[i][j] != 0:
                    f.write(str(int(mat[i][j])))
                if j != len(mat[i])-1:
                    f.write(',')
            f.write('\n')

writeMatToCSV(adjMatArr)
